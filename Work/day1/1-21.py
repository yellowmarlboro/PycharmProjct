import turtle
turtle.penup()
turtle.goto(0,-100)
turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.goto(0,100)
turtle.write("12")
turtle.penup()
turtle.goto(100,0)
turtle.write("3")
turtle.penup()
turtle.goto(-17,-100)
turtle.write("     6 \n 9:15:00")
turtle.penup()
turtle.goto(-100,0)
turtle.write(" 9")
turtle.penup()
turtle.goto(0,75)
turtle.pendown()
turtle.right(90)
turtle.goto(0,0)
turtle.goto(50,0)
turtle.done()